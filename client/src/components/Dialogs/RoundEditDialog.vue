<template>
  <the-dialog
    activator-icon="pencil"
    color="#607d8b"
    header="Edit round info"
    simple
    :submit-logic="onSubmit"
  >
    <v-switch v-model="toDelete" label="Delete round" color="error" hide-details />
  </the-dialog>
</template>

<script>
import { mapActions } from "vuex";

export default {
  name: "RoundEditDialog",
  props: {
    round: {
      type: Object,
      required: true
    }
  },
  data() {
    return {
      toDelete: false
    };
  },
  methods: {
    ...mapActions("rounds", ["deleteRound"]),
    onSubmit() {
      this.toDelete && this.deleteRound(this.round._id);
    }
  }
};
</script>
